'''

 Read an integer ‘m’ and if the value is greater than 0 display 1, if it is equal to 0 , display 0 and-1 if the value is less than 0 display-1

'''



m = int(input("Enter an integer number : "))

if m>0:
    print("1")
elif m<0: 
    print("-1")
else: 
    print("0")


'''
OUTPUT : 

Enter an integer number : 65
1

'''