'''

Write a python code to find the maximum and minimum values in a set

'''

set_1 = {1,2,2,3,4,5,6,7,8,9}

maximum = max(set_1)
minimum = min(set_1)

print(f"The maximum element in {set_1} is {maximum}")
print(f"The minimum element in {set_1} is {minimum}")


'''

OUTPUT : 

The maximum element in {1, 2, 3, 4, 5, 6, 7, 8, 9} is 9
The minimum element in {1, 2, 3, 4, 5, 6, 7, 8, 9} is 1

'''